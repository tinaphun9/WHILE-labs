# This program uses a while loop to count from 0 up to (but not including) 10

def main():
    print("Starting While Loop - Print Count Variable")

    number = 0  #to start counting from 0

    while number < 10:#to count from number(0) to 9
        print(number)
        number += 1   #to increase by 1 each time

    print("Ending While Loop")

main()
